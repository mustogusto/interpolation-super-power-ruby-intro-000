def display_rainbow(color)
  color = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
  puts "R:#{color[0]}"
  puts "O:#{color[1]}"
  puts "Y:#{color[2]}"
  puts "G:#{color[3]}"
  puts "B:#{color[4]}"
  puts "I:#{color[5]}"
  puts "V:#{color[6]}"
end

display_rainbow('color[0]', 'color[1]', 'color[2]', 'color[3]', 'color[4]', 'color[5]', 'color[6]')
